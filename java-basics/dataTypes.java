class DataTypesDemo {

    public static void main(String[] args) {

        // Integer types
        byte age = 20;
        short year = 2025;
        int salary = 50000;
        long population = 14000000000L;

        // Decimal types
        float pi = 3.14f;
        double largeDecimal = 12345.6789;

        // Character type
        char grade = 'A';

        // Boolean type
        boolean isJavaFun = true;

        // Printing values
        System.out.println("Byte value: " + age);
        System.out.println("Short value: " + year);
        System.out.println("Int value: " + salary);
        System.out.println("Long value: " + population);

        System.out.println("Float value: " + pi);
        System.out.println("Double value: " + largeDecimal);

        System.out.println("Char value: " + grade);

        System.out.println("Boolean value: " + isJavaFun);
    }
}